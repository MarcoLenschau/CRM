/**
 * Button type for HTML button elements.
 * Union type supporting standard HTML button types: submit, reset, button, or undefined.
 *
 * @typedef {("submit" | "reset" | "button" | undefined)} ButtonType
 * @category Types
 * @author Marco Lenschau <contact@marco-lenschau.de>
 */
export type ButtonType = "submit" | "reset" | "button" | undefined;